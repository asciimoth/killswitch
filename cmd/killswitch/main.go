func readBootstrapEvents(reader *ringbuf.Reader, notifyError func(string, error)) error {
	for {
		record, err := reader.Read()
		if err != nil {
			return err
		}

		event, err := parseBootstrapEvent(record.RawSample)
		if err != nil {
			log.Printf("parse bootstrap event: %s", err)
			if notifyError != nil {
				notifyError("Bootstrap event parse error", err)
			}
			continue
		}
		log.Print(formatBootstrapEvent(event))
	}
}

func parseBootstrapEvent(raw []byte) (bootstrapEvent, error) {
	var event bootstrapEvent
	if err := binary.Read(bytes.NewReader(raw), binary.LittleEndian, &event); err != nil {
		return bootstrapEvent{}, err
	}
	return event, nil
}

func formatBootstrapEvent(event bootstrapEvent) string {
	reason := "unknown"
	switch event.Reason {
	case bootstrapARP:
		reason = "arp"
	case bootstrapDHCPv4:
		reason = "dhcpv4"
	case bootstrapDHCPv6:
		reason = "dhcpv6"
	case bootstrapICMPv6:
		reason = "icmpv6_nd"
	}

	if event.Reason == bootstrapDHCPv4 {
		return fmt.Sprintf("bootstrap pass: reason=%s ifindex=%d src=%s:%d dst=%s:%d",
			reason,
			event.Ifindex,
			ipv4FromNetworkOrder(event.IPv4Saddr),
			ntohs(event.SourcePort),
			ipv4FromNetworkOrder(event.IPv4Daddr),
			ntohs(event.DestPort),
		)
	}
	if event.Reason == bootstrapDHCPv6 {
		return fmt.Sprintf("bootstrap pass: reason=%s ifindex=%d src=[%s]:%d dst=[%s]:%d vlan_depth=%d",
			reason,
			event.Ifindex,
			net.IP(event.IPv6Saddr[:]),
			ntohs(event.SourcePort),
			net.IP(event.IPv6Daddr[:]),
			ntohs(event.DestPort),
			event.VLANDepth,
		)
	}
	if event.Reason == bootstrapICMPv6 {
		return fmt.Sprintf("bootstrap pass: reason=%s ifindex=%d src=%s dst=%s type=%d vlan_depth=%d",
			reason,
			event.Ifindex,
			net.IP(event.IPv6Saddr[:]),
			net.IP(event.IPv6Daddr[:]),
			event.ICMPv6Type,
			event.VLANDepth,
		)
	}

	return fmt.Sprintf("bootstrap pass: reason=%s ifindex=%d eth_proto=0x%04x vlan_depth=%d",
		reason,
		event.Ifindex,
		event.EthProto,
		event.VLANDepth,
	)
}

func newEgressManager(program *ebpf.Program) *egressManager {
	return &egressManager{
		program:  program,
		attached: make(map[int]attachedInterface),
	}
}

func (m *egressManager) reconcileCurrent(opts options, strict bool) (bool, error) {
	selected, err := selectedInterfaces(opts)
	if err != nil {
		return false, err
	}
	if len(selected) == 0 {
		log.Print("No interfaces currently match the configured selectors")
	}
	return m.reconcile(selected, strict)
}

func (m *egressManager) reconcile(selected []interfaceInfo, strict bool) (bool, error) {
	m.mu.Lock()
	defer m.mu.Unlock()

	desired := make(map[int]interfaceInfo, len(selected))
	for _, iface := range selected {
		desired[iface.Index] = iface
	}

	changed := false
	for index, attached := range m.attached {
		if _, ok := desired[index]; ok {
			continue
		}
		if err := attached.link.Close(); err != nil {
			log.Printf("ERROR: detach tc egress program from %s(index %d type %s): %s", attached.info.Name, attached.info.Index, attached.info.Type, err)
		} else {
			log.Printf("Detached tc egress program from %s(index %d type %s)", attached.info.Name, attached.info.Index, attached.info.Type)
		}
		delete(m.attached, index)
		changed = true
	}

	var attachErr error
	for _, iface := range selected {
		if attached, ok := m.attached[iface.Index]; ok {
			attached.info = iface
			m.attached[iface.Index] = attached
			continue
		}

		l, err := link.AttachTCX(link.TCXOptions{
			Interface: iface.Index,
			Program:   m.program,
			Attach:    ebpf.AttachTCXEgress,
		})
		if err != nil {
			err = fmt.Errorf("attach tc egress program to %s(index %d type %s): %w", iface.Name, iface.Index, iface.Type, err)
			log.Printf("ERROR: %s", err)
			attachErr = errors.Join(attachErr, err)
			continue
		}
		m.attached[iface.Index] = attachedInterface{link: l, info: iface}
		log.Printf("Attached tc egress program to %s(index %d type %s)", iface.Name, iface.Index, iface.Type)
		changed = true
	}

	if changed {
		log.Printf("Kill switch attached to: %s", interfaceNames(m.attachedInterfacesLocked()))
	}
	if strict {
		return changed, attachErr
	}
	return changed, nil
}

func (m *egressManager) attachedInterfacesLocked() []interfaceInfo {
	ifaces := make([]interfaceInfo, 0, len(m.attached))
	for _, attached := range m.attached {
		ifaces = append(ifaces, attached.info)
	}
	sort.Slice(ifaces, func(i, j int) bool {
		return ifaces[i].Name < ifaces[j].Name
	})
	return ifaces
}

func (m *egressManager) close() {
	m.mu.Lock()
	defer m.mu.Unlock()
	for index, attached := range m.attached {
		if err := attached.link.Close(); err != nil {
			log.Printf("closing link for %s(index %d): %s", attached.info.Name, attached.info.Index, err)
		}
		delete(m.attached, index)
	}
}

func watchInterfaces(ctx context.Context, manager *egressManager, policies *policyManager, reconcileMu *sync.Mutex, notify func(adminapi.EventType)) error {
	linkUpdates := make(chan netlink.LinkUpdate, 32)
	addrUpdates := make(chan netlink.AddrUpdate, 32)
	done := make(chan struct{})
	defer close(done)

	subscribeErrs := make(chan error, 1)
	errCallback := func(err error) {
		select {
		case subscribeErrs <- err:
		default:
			log.Printf("ERROR: netlink link watcher: %s", err)
		}
	}

	if err := netlink.LinkSubscribeWithOptions(linkUpdates, done, netlink.LinkSubscribeOptions{
		ErrorCallback: errCallback,
	}); err != nil {
		return fmt.Errorf("subscribe to netlink link updates: %w", err)
	}
	if err := netlink.AddrSubscribeWithOptions(addrUpdates, done, netlink.AddrSubscribeOptions{
		ErrorCallback: errCallback,
	}); err != nil {
		return fmt.Errorf("subscribe to netlink addr updates: %w", err)
	}

	if _, err := manager.reconcileCurrent(policies.optionsSnapshot(), true); err != nil {
		return err
	}
	policyChanged, err := policies.reconcileAttached(manager, true)
	if err != nil {
		return err
	}
	if notify != nil {
		notify(adminapi.EventTypeInterfaces)
		if policyChanged {
			notify(adminapi.EventTypeConfig)
		}
	}

	for {
		select {
		case <-ctx.Done():
			return nil
		case err := <-subscribeErrs:
			return fmt.Errorf("netlink link watcher: %w", err)
		case update := <-linkUpdates:
			reconcileMu.Lock()
			opts := policies.optionsSnapshot()
			shouldReconcileAttachments := manager.shouldReconcileLinkUpdate(update, opts)
			interfacesChanged := false
			if shouldReconcileAttachments {
				var err error
				interfacesChanged, err = manager.reconcileCurrent(opts, false)
				if err != nil {
					log.Printf("ERROR: reconcile interfaces after netlink update: %s", err)
				}
			}
			policyChanged, err := policies.reconcileAttached(manager, true)
			if err != nil {
				log.Printf("ERROR: reconcile rulesets after netlink link update: %s", err)
			}
			reconcileMu.Unlock()
			if notify != nil {
				if interfacesChanged {
					notify(adminapi.EventTypeInterfaces)
				}
				if policyChanged {
					notify(adminapi.EventTypeConfig)
				}
			}
		case <-addrUpdates:
			reconcileMu.Lock()
			policyChanged, err := policies.reconcileAttached(manager, true)
			if err != nil {
				log.Printf("ERROR: reconcile rulesets after netlink addr update: %s", err)
			}
			reconcileMu.Unlock()
			if notify != nil {
				if policyChanged {
					notify(adminapi.EventTypeInterfaces)
					notify(adminapi.EventTypeConfig)
				}
			}
		}
	}
}

func (m *egressManager) shouldReconcileLinkUpdate(update netlink.LinkUpdate, opts options) bool {
	if update.Link == nil || update.Link.Attrs() == nil { //nolint:staticcheck
		return true
	}

	attrs := update.Link.Attrs() //nolint:staticcheck
	switch update.Header.Type {
	case unix.RTM_DELLINK:
		return m.isAttached(attrs.Index)
	case unix.RTM_NEWLINK:
		matches, err := interfaceMatchesSelectors(interfaceInfo{
			Index: attrs.Index,
			Name:  attrs.Name,
			Type:  update.Link.Type(), //nolint:staticcheck
		}, opts)
		if err != nil {
			log.Printf("ERROR: match netlink link update selectors: %s", err)
			return true
		}
		return matches != m.isAttached(attrs.Index)
	default:
		return false
	}
}

func (m *egressManager) isAttached(index int) bool {
	m.mu.Lock()
	defer m.mu.Unlock()
	_, ok := m.attached[index]
	return ok
}

func interfaceMatchesSelectors(iface interfaceInfo, opts options) (bool, error) {
	ignored, err := interfaceMatchesIgnoreSelectors(iface, opts)
	if err != nil {
		return false, err
	}
	if ignored {
		return false, nil
	}

	for _, typ := range opts.InterfaceTypes {
		if iface.Type == typ {
			return true, nil
		}
	}
	for _, name := range opts.InterfaceNames {
		if iface.Name == name {
			return true, nil
		}
	}
	for _, pattern := range opts.InterfaceRegexps {
		matches, err := regexp.MatchString(pattern, iface.Name)
		if err != nil {
			return false, fmt.Errorf("compile interface regexp %q: %w", pattern, err)
		}
		if matches {
			return true, nil
		}
	}
	return false, nil
}

func interfaceMatchesIgnoreSelectors(iface interfaceInfo, opts options) (bool, error) {
	if iface.Name == "lo" {
		return true, nil
	}
	for _, typ := range opts.IgnoredInterfaceTypes {
		if iface.Type == typ {
			return true, nil
		}
	}
	for _, name := range opts.IgnoredInterfaceNames {
		if iface.Name == name {
			return true, nil
		}
	}
	for _, pattern := range opts.IgnoredInterfaceRegexps {
		matches, err := regexp.MatchString(pattern, iface.Name)
		if err != nil {
			return false, fmt.Errorf("compile ignored interface regexp %q: %w", pattern, err)
		}
		if matches {
			return true, nil
		}
	}
	return false, nil
}

func interfaceNames(ifaces []interfaceInfo) string {
	names := make([]string, 0, len(ifaces))
	for _, iface := range ifaces {
		names = append(names, iface.Name)
	}
	return strings.Join(names, ", ")
}

func boolByte(value bool) uint8 {
	if value {
		return 1
	}
	return 0
}

func ntohs(value uint16) uint16 {
	return value<<8 | value>>8
}

func htons(value uint16) uint16 {
	return ntohs(value)
}

func ipv4FromNetworkOrder(value uint32) net.IP {
	return net.IPv4(byte(value), byte(value>>8), byte(value>>16), byte(value>>24))
}

func ipv4Key(addr netip.Addr) uint32 {
	octets := addr.As4()
	return binary.LittleEndian.Uint32(octets[:])
}

func ipv6Key(addr netip.Addr) ipv6AddrKey {
	var key ipv6AddrKey
	octets := addr.As16()
	copy(key.Addr[:], octets[:])
	return key
}
