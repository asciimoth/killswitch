func cloneTemporaryRulesets(rulesets map[string]temporaryRuleset) []temporaryRuleset {
	if len(rulesets) == 0 {
		return nil
	}
	owners := make([]string, 0, len(rulesets))
	for owner := range rulesets {
		owners = append(owners, owner)
	}
	sort.Strings(owners)

	out := make([]temporaryRuleset, 0, len(owners))
	for _, owner := range owners {
		tmp := rulesets[owner]
		out = append(out, temporaryRuleset{
			Owner:      owner,
			Interfaces: cloneStrings(tmp.Interfaces),
			Rules:      cloneAllowRules(tmp.Rules),
		})
	}
	return out
}

func cloneForceRulesets(rulesets map[string]map[string]map[string]int) []forceRuleset {
	if len(rulesets) == 0 {
		return nil
	}
	names := make([]string, 0, len(rulesets))
	for name := range rulesets {
		if len(rulesets[name]) > 0 {
			names = append(names, name)
		}
	}
	sort.Strings(names)

	out := make([]forceRuleset, 0, len(names))
	for _, name := range names {
		ownerSet := make(map[string]bool)
		ifaceSet := make(map[string]bool)
		for owner, ifaces := range rulesets[name] {
			ownerSet[owner] = true
			for iface := range ifaces {
				ifaceSet[iface] = true
			}
		}
		owners := make([]string, 0, len(ownerSet))
		for owner := range ownerSet {
			owners = append(owners, owner)
		}
		sort.Strings(owners)
		ifaces := make([]string, 0, len(ifaceSet))
		for iface := range ifaceSet {
			ifaces = append(ifaces, iface)
		}
		sort.Strings(ifaces)
		out = append(out, forceRuleset{
			Name:       name,
			Owners:     owners,
			Interfaces: ifaces,
		})
	}
	return out
}

func run(parent context.Context, opts options) error {
	if err := rlimit.RemoveMemlock(); err != nil {
		return fmt.Errorf("remove memlock rlimit: %w", err)
	}

	var objs killswitchObjects
	if err := loadKillswitchObjects(&objs, nil); err != nil {
		return fmt.Errorf("load eBPF objects: %w", err)
	}
	defer objs.Close() //nolint:errcheck

	manager := newEgressManager(objs.KillswitchEgress)
	defer manager.close()
	policies := newPolicyManager(&objs, opts)

	signalCtx, stop := signal.NotifyContext(parent, os.Interrupt, syscall.SIGTERM)
	defer stop()
	ctx, cancel := context.WithCancel(signalCtx)
	defer cancel()

	reader, err := ringbuf.NewReader(objs.BootstrapEvents)
	if err != nil {
		return fmt.Errorf("open bootstrap event ring buffer: %w", err)
	}
	defer reader.Close() //nolint:errcheck

	go func() {
		<-ctx.Done()
		_ = reader.Close()
	}()

	var reconcileMu sync.Mutex

	var adminServer *adminAPIServer
	notifyError := func(header string, err error) {
		if adminServer != nil {
			adminServer.notifyError(header, err)
		}
	}

	configSnapshot := func() adminapi.CurrentConfig {
		return currentConfigSnapshot(policies, manager, adminServer.clientSnapshot, notifyError)
	}
	adminServer = newAdminAPIServer(opts.AdminAPI, configSnapshot, func(req adminapi.MutationRequest) adminapi.MutationResult {
		return applyAdminMutation(req, policies, manager, &reconcileMu)
	})

	errCh := make(chan error, 3)
	go func() {
		if err := readBootstrapEvents(reader, notifyError); err != nil && !errors.Is(err, ringbuf.ErrClosed) {
			errCh <- err
		}
	}()
	adminServer.setTemporaryRulesetCallbacks(
		func(owner string, req adminapi.MutationRequest) adminapi.MutationResult {
			return applyTemporaryRulesetMutation(owner, req, policies, manager, &reconcileMu)
		},
		func(owner string) adminapi.MutationResult {
			return removeTemporaryRuleset(owner, policies, manager, &reconcileMu)
		},
	)
	adminServer.setForceRulesetCallbacks(
		func(owner string, req adminapi.MutationRequest) adminapi.MutationResult {
			return applyForceRulesetMutation(owner, req, policies, manager, &reconcileMu)
		},
		func(owner string) adminapi.MutationResult {
			return removeForceRulesets(owner, policies, manager, &reconcileMu)
		},
	)
	go func() {
		if err := watchInterfaces(ctx, manager, policies, &reconcileMu, func(eventType adminapi.EventType) {
			adminServer.notify(eventType)
		}); err != nil {
			errCh <- err
		}
	}()
	go func() {
		if err := adminServer.listenAndServe(ctx); err != nil {
			errCh <- err
		}
	}()

	select {
	case <-ctx.Done():
		return nil
	case err := <-errCh:
		cancel()
		return err
	}
}

func selectedInterfaces(opts options) ([]interfaceInfo, error) {
	all, err := listInterfaces()
	if err != nil {
		return nil, fmt.Errorf("list interfaces: %w", err)
	}
	return selectInterfaces(all, opts)
}

func listInterfaces() ([]interfaceInfo, error) {
	links, err := netlink.LinkList()
	if err != nil {
		return nil, err
	}

	all := make([]interfaceInfo, 0, len(links))
	for _, l := range links {
		attrs := l.Attrs()
		if attrs == nil {
			continue
		}
		addrs, err := interfaceAddrs(l)
		if err != nil {
			return nil, err
		}
		all = append(all, interfaceInfo{
			Index: attrs.Index,
			Name:  attrs.Name,
			Type:  l.Type(),
			Addrs: addrs,
		})
	}
	return all, nil
}

func interfaceAddrs(link netlink.Link) ([]netip.Addr, error) {
	addrList, err := netlink.AddrList(link, netlink.FAMILY_ALL)
	if err != nil {
		attrs := link.Attrs()
		if attrs != nil {
			return nil, fmt.Errorf("list addresses for %s(index %d): %w", attrs.Name, attrs.Index, err)
		}
		return nil, fmt.Errorf("list addresses: %w", err)
	}

	addrs := make([]netip.Addr, 0, len(addrList))
	for _, addr := range addrList {
		parsed, ok := netipAddrFromIP(addr.IP)
		if ok {
			addrs = append(addrs, parsed)
		}
	}
	sort.Slice(addrs, func(i, j int) bool {
		return addrs[i].Less(addrs[j])
	})
	return addrs, nil
}

func netipAddrFromIP(ip net.IP) (netip.Addr, bool) {
	if v4 := ip.To4(); v4 != nil {
		addr, ok := netip.AddrFromSlice(v4)
		return addr.Unmap(), ok
	}
	if v6 := ip.To16(); v6 != nil {
		addr, ok := netip.AddrFromSlice(v6)
		return addr.Unmap(), ok
	}
	return netip.Addr{}, false
}

func selectInterfaces(all []interfaceInfo, opts options) ([]interfaceInfo, error) {
	var selected []interfaceInfo
	for _, iface := range all {
		matches, err := interfaceMatchesSelectors(iface, opts)
		if err != nil {
			return nil, err
		}
		if matches {
			selected = append(selected, iface)
		}
	}

	sort.Slice(selected, func(i, j int) bool {
		return selected[i].Name < selected[j].Name
	})
	return selected, nil
}

func newPolicyManager(objs *killswitchObjects, opts options) *policyManager {
	return &policyManager{objs: objs, opts: opts}
}

func (m *policyManager) optionsSnapshot() options {
	m.mu.Lock()
	defer m.mu.Unlock()
	return cloneOptions(m.opts)
}

func (m *policyManager) replaceOptions(opts options) {
	m.mu.Lock()
	defer m.mu.Unlock()
	m.opts = cloneOptions(opts)
	m.pruneForceRulesetsLocked()
}

func (m *policyManager) temporaryRulesetsSnapshot() []temporaryRuleset {
	m.mu.Lock()
	defer m.mu.Unlock()
	return cloneTemporaryRulesets(m.tmpRulesets)
}

func (m *policyManager) forceRulesetsSnapshot() []forceRuleset {
	m.mu.Lock()
	defer m.mu.Unlock()
	return cloneForceRulesets(m.forceRulesets)
}

func (m *policyManager) setTemporaryRuleset(owner string, interfaces []string, rules allowRules) {
	m.mu.Lock()
	defer m.mu.Unlock()
	if m.tmpRulesets == nil {
		m.tmpRulesets = make(map[string]temporaryRuleset)
	}
	m.tmpRulesets[owner] = temporaryRuleset{Owner: owner, Interfaces: cloneStrings(interfaces), Rules: cloneAllowRules(rules)}
}

func (m *policyManager) removeTemporaryRuleset(owner string) bool {
	m.mu.Lock()
	defer m.mu.Unlock()
	if _, ok := m.tmpRulesets[owner]; !ok {
		return false
	}
	delete(m.tmpRulesets, owner)
	return true
}

func (m *policyManager) forceActivateRuleset(owner, name string, interfaces []string, replace bool) bool {
	m.mu.Lock()
	defer m.mu.Unlock()
	if findRuleset(m.opts.Rulesets, name) < 0 {
		return false
	}
	if m.forceRulesets == nil {
		m.forceRulesets = make(map[string]map[string]map[string]int)
	}
	owners := m.forceRulesets[name]
	if owners == nil {
		owners = make(map[string]map[string]int)
		m.forceRulesets[name] = owners
	}
	if owners[owner] == nil {
		owners[owner] = make(map[string]int)
	}
	if replace {
		clear(owners[owner])
	}
	for _, iface := range interfaces {
		owners[owner][iface]++
	}
	return true
}

func (m *policyManager) releaseForceRuleset(owner, name string, interfaces []string) bool {
	m.mu.Lock()
	defer m.mu.Unlock()
	return m.releaseForceRulesetLocked(owner, name, interfaces)
}

func (m *policyManager) removeForceRulesets(owner string) bool {
	m.mu.Lock()
	defer m.mu.Unlock()
	changed := false
	for name := range m.forceRulesets {
		if m.releaseAllForceRulesetLocked(owner, name) {
			changed = true
		}
	}
	return changed
}

func (m *policyManager) releaseForceRulesetLocked(owner, name string, interfaces []string) bool {
	owners := m.forceRulesets[name]
	if owners == nil || len(owners[owner]) == 0 {
		return false
	}
	if len(interfaces) == 0 {
		delete(owners, owner)
	} else {
		for _, iface := range interfaces {
			if owners[owner][iface] <= 1 {
				delete(owners[owner], iface)
			} else {
				owners[owner][iface]--
			}
		}
		if len(owners[owner]) == 0 {
			delete(owners, owner)
		}
	}
	if len(owners) == 0 {
		delete(m.forceRulesets, name)
	}
	return true
}

func (m *policyManager) releaseAllForceRulesetLocked(owner, name string) bool {
	owners := m.forceRulesets[name]
	if owners == nil || len(owners[owner]) == 0 {
		return false
	}
	delete(owners, owner)
	if len(owners) == 0 {
		delete(m.forceRulesets, name)
	}
	return true
}

func (m *policyManager) pruneForceRulesetsLocked() {
	if len(m.forceRulesets) == 0 {
		return
	}
	for name := range m.forceRulesets {
		if findRuleset(m.opts.Rulesets, name) < 0 {
			delete(m.forceRulesets, name)
		}
	}
}

func (m *policyManager) reconcileAttached(manager *egressManager, logChange bool) (bool, error) {
	if manager == nil {
		return m.reconcile(nil, logChange)
	}
	attached := manager.attachedIndexSnapshot()
	if len(attached) == 0 {
		return m.reconcile(nil, logChange)
	}
	all, err := listInterfaces()
	if err != nil {
		return false, fmt.Errorf("list interfaces: %w", err)
	}
	current := make([]interfaceInfo, 0, len(attached))
	for _, iface := range all {
		if attached[iface.Index] {
			current = append(current, iface)
		}
	}
	return m.reconcile(current, logChange)
}

func (m *policyManager) reconcile(all []interfaceInfo, logChange bool) (bool, error) {
	opts := m.optionsSnapshot()
	tmpRulesets := m.temporaryRulesetsSnapshot()
	forceRulesets := m.forceRulesetsSnapshot()
	selected, err := selectInterfaces(all, opts)
	if err != nil {
		return false, err
	}
	next := effectivePoliciesForInterfaces(selected, opts, tmpRulesets, forceRulesets)

	m.mu.Lock()
	defer m.mu.Unlock()
	if m.set && interfacePolicyRulesEqual(m.current, next) {
		m.current = cloneInterfacePolicyMap(next)
		return false, nil
	}

	if err := writeEffectivePolicies(m.objs, m.current, next); err != nil {
		return false, err
	}
	m.current = cloneInterfacePolicyMap(next)
	m.set = true
	if logChange {
		logPolicies(next)
	}
	return true, nil
}

func (m *policyManager) configSnapshot() adminapi.CurrentConfig {
	m.mu.Lock()
	current := cloneInterfacePolicyMap(m.current)
	set := m.set
	opts := cloneOptions(m.opts)
	tmpRulesets := cloneTemporaryRulesets(m.tmpRulesets)
	forceRulesets := cloneForceRulesets(m.forceRulesets)
	m.mu.Unlock()

	if !set {
		current = nil
	}
	return adminapi.CurrentConfig{
		InterfaceTypes:          cloneStrings(opts.InterfaceTypes),
		InterfaceNames:          cloneStrings(opts.InterfaceNames),
		InterfaceRegexps:        cloneStrings(opts.InterfaceRegexps),
		IgnoredInterfaceTypes:   cloneStrings(opts.IgnoredInterfaceTypes),
		IgnoredInterfaceNames:   cloneStrings(opts.IgnoredInterfaceNames),
		IgnoredInterfaceRegexps: cloneStrings(opts.IgnoredInterfaceRegexps),
		BasePolicy:              apiAllowRules(opts.allowRules),
		EffectivePolicy:         apiAllowRules(firstEffectivePolicy(current, opts.allowRules)),
		ActiveRuleset:           firstActiveRuleset(current),
		EffectiveInterfaces:     apiInterfacePolicies(current),
		Rulesets:                apiRulesets(opts.Rulesets, activeRulesetSet(current)),
		ForceActiveRulesets:     apiForceRulesets(forceRulesets),
		TemporaryRulesets:       apiTemporaryRulesets(tmpRulesets),
		AdminAPI: adminapi.AdminConfig{
			SocketPath: opts.AdminAPI.SocketPath,
			Debug:      opts.AdminAPI.Debug,
		},
	}
}

